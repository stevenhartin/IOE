	//////////////////////////////////////////////////////////////////////////

	IOEVector IOEVector::Multiply(const IOEMatrix &rhs) const
	{
		IOEVector vTemp(*this);
		vTemp.MultiplySet(rhs);
		return vTemp;
	}

	//////////////////////////////////////////////////////////////////////////

	void IOEVector::MultiplySet(const IOEMatrix &rhs)
	{
		m_vVector = XMVector4Transform(m_vVector, rhs.GetRaw());
	}

	//////////////////////////////////////////////////////////////////////////

	IOEVector &IOEVector::operator*=(const IOEMatrix &rhs)
	{
		MultiplySet(rhs);
		return *this;
	}

	//////////////////////////////////////////////////////////////////////////

	IOEVector IOEVector::operator*(const IOEMatrix &rhs) const
	{
		return Multiply(rhs);
	}

